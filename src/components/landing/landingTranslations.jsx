// Hook to use landing translations
export function useLandingLanguage() {
  const getStoredLanguage = () => {
    if (typeof window !== 'undefined') {
      return localStorage.getItem('landingLanguage') || 'he';
    }
    return 'he';
  };

  return {
    getLanguage: getStoredLanguage,
    setLanguage: (lang) => {
      localStorage.setItem('landingLanguage', lang);
      window.dispatchEvent(new Event('landingLanguageChange'));
    },
  };
}